    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTable1.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "You must select at least one track",
                    "ERROR: Not selected track", JOptionPane.ERROR_MESSAGE);
        } else if (jTable1.getSelectedRowCount() == 1) {
            shoppingList.insertTrack(selectedSong);
            JOptionPane.showMessageDialog(this, "One track has been added",
                    "Track added", JOptionPane.INFORMATION_MESSAGE);
        } else {
            boolean inserted = false;
            for (Track t : selectedTracks) {
                if (!shoppingList.existTrack(t)) {
                    shoppingList.insertTrack(t);
                    inserted = true;
                }
            }
            if (inserted) {
                JOptionPane.showMessageDialog(this, "All selected tracks have been added",
                        "Tracks added", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "the selected track(s) ha(s/ve) been already added",
                        "Duplicate tracks", JOptionPane.WARNING_MESSAGE);
            }
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


